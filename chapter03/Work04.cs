using System;

class Work04 {
    static void Main(string[] args)
    {
        int a = 2;
        
        Console.WriteLine(a); // 2
        
        a += 2;
        Console.WriteLine(a); // 4
        
        a -= 1;
        Console.WriteLine(a); // 3

        a *= 2;
        Console.WriteLine(a); // 6

        a /= 2;
        Console.WriteLine(a); // 3
    }
}
