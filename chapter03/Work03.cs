using System;

class Work03 {
    static void Main(string[] args)
    {
        int a = 1;
        Console.WriteLine(a); // 1

        a++;
        Console.WriteLine(a); // 2
        
        a++;
        Console.WriteLine(a); // 3
        
        a--;
        Console.WriteLine(a); // 2

        a--;
        Console.WriteLine(a); // 1
    }
}
