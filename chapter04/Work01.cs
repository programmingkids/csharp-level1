using System;

class Work01 {
    static void Main(string[] args)
    {
        // ==
        Console.WriteLine( 3 == 3 ); 
        
        // !=
        Console.WriteLine( 3 != 4 ); 

        // > >=
        Console.WriteLine( 3 > 2 ); 
        Console.WriteLine( 3 >= 3 ); 

        // < <=
        Console.WriteLine( 3 < 4 ); 
        Console.WriteLine( 3 <= 3 ); 
    }
}
